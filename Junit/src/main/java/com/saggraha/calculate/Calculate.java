package com.saggraha.calculate;

public class Calculate {
	
	public Integer performAddition(Integer a, Integer b) {
		return a+b;
	}
	
	public Integer performSubtraction(Integer a, Integer b) {
		return a-b;
	}
	
	public Integer performMultiplication(Integer a, Integer b) {
		return a*b;
	}
	
	public Integer performDivision(Integer a, Integer b) {
		return a/b;
	}
}
